      NEQ=KZrowsc+(Nzon-Knth)
      else
      NEQ=krow
      endif
      If(KNadap.EQ.2.or.KNadap.EQ.3)then
      write(*,*)' freqs not geom. progr.!! '
      stop52
      endif
      zerfr=log(freqmn(1))+1.d0/dlognu(1)
      DO09949Kpl=Nzon,incnd+1,-1
      DO09946Ip=Nmu(Kpl),0,-1
      fr0=freqmn(Lfr)
      chiab=HAPabW(Kpl,Lfr)
