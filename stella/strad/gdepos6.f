      COMMON/UNBSTL/UR,UM,UEPRI,ULGP,ULGE,ULGV,ULGTM,ULGEST,ULGFL,ULGCAP,ULGEPS
      Parameter(tret=1.d0/3.d0)
      Parameter(Nfrgam=1)
      Parameter(shrink=0.9d0)
      Parameter(Capgam=5.d-02)
      COMMON/CONUR/EIT,DST,BBRCNR(5)
      COMMON/BAL/EL(MAXDER+1),YENTOT(MAXDER+1),ETOT0,ELVOL,ELSURF,ELTOT,TPSURF,HOLDBL,ELOST,EKO,RADBEG
      common/NSTEP/NSTEP,NDebug,MAXER,IOUT,NOUT
      common/CREAD/TAUOLD,NSTMAX,MBATCH,MAXORD
      common/debug/LfrDebug,Nperturb,Kbad
      REAL*8TPMAX(MAXDER+1),TQ(4)
      COMMON/TAU/TAU(Mzon+1),FLUX(Mzon)
      common/tauubvri/tauU(Mzon),tauB(Mzon),tauV(Mzon),tauR(Mzon),tauI(Mzon)
      COMMON/PHOT/XJPH,DMPH,RPH,TPH,PLPH,VPH,CHEMPH,GRVPH,HP,JPH
      PARAMETER(NFUNC=6)
      REAL*4WORK(Mzon+2,NFREQ),WRK(Mzon,4)
      REAL*8WRKX(Mzon),WORKX(Mzon+2)
      COMMON/STEPD/WRKX,WORKX,TPHOT,TEFF,WORK,WRK,NPHOT,NZM
      PARAMETER(TMCRIT=1.D-6,TPNSE=5.D0,EPGROW=0.02D0)
      Common/RUTP/Ry(Mzon),Uy(Mzon),Ty(Mzon),Press(Mzon),Rho(Mzon)
      COMMON/TOO/TOO,KO,KNTO,TO(KOMAX),STO(KOMAX),NTO(KOMAX)
      Parameter(Lcurdm=1000)
      RealTcurv
      IntegerNFRUSED
      REAL*8Flsave
      Common/Curve/tcurv(8,Lcurdm),Depos(Lcurdm),Flsave(MFREQ+1,Lcurdm),NFRUSED(Lcurdm),Lsaved
      LOGICALBEGRUN
      Common/BEGR/BEGRUN
      CHARACTER*80Model,Sumprf,Sumcur,Depfile,Flxfile
      COMMON/Files/Model,Sumprf,Sumcur,Depfile,Flxfile
      CHARACTER*1app
      LogicalGivdtl
      Common/ABGrap/NSTA,NSTB,TcurA,TcurB,Givdtl
      REAL*8MBOL,MU,MB,MV,MR,MI,MBOL1
      COMMON/COLOR/MBOL,MU,MB,MV,MR,MI,UMB,BMV,MBOL1,LubvU,LubvB,LubvV,LubvR,LubvI,Lyman
      COMMON/DETAIL/QRTarr(Mzon),UUarr(Mzon),ArrLum(Mzon),Acc(Mzon)
      Common/XYZ/XA,YA,URM
      Integersm_device
      Character*40chalab
      DimensionRMPray(Mzon)
      Dimensiondtau(Mzon,0:Np)
      Real*4xipl(0:Np),fspl(0:Np),ftst(0:Np),fmin,fmax
      DimensionZR(0:Mzon)
      DimensionRM(0:Mzon),umr(0:Mzon)
      DimensionRHS(2*Mzon+2)
      DimensionAmu(0:NP+1,0:Mzon)
      DimensionFSdir(0:NP+1,Mzon)
      DimensionKz(0:NP+1),Krowst(0:NP+1)
      DimensionNMU(Mzon)
      DimensionYe(Mzon)
      DimensionHAPW(Mzon,Nfrgam)
      DimensionHAPabW(Mzon,Nfrgam)
      DimensionHAPM(Mzon),HAPabM(Mzon),HAPabZ(Mzon)
      DimensionHAPZ(Mzon)
      DimensionTAUGAM(Mzon)
      DimensionChim(Mzon),Chiabm(Mzon),ChiabZ(Mzon)
      DimensionChiZ(Mzon)
      Realtime1,time2
      Realtm1,tm2
      LogicalScatG
      Parameter(Nchn=4)
      DataScatG/.False./
      If(toldg.GT.0.d0.AND.t.GT.toldg*1.003d0)then
      DO09997km=1,Nzon
      FJgam(km,1)=Fjgam(km,2)
09997 CONTINUE
      toldg=tnewg
      Ngamma=2
      Else
      toldg=t
      Ngamma=2
      endif
      DO09994Igamma=2,Ngamma,-1
      If(Igamma.EQ.1)Then
      Homo=0.d0
      DO09991km=1,Nzon
      Homo=Homo+(t-toldg)*ABS(Uy(km))/Ry(km)*dm(km)/(Amout-Amini)
09991 CONTINUE
      Homo=1.d0+Homo
      Else
      Homo=1.d0
      Endif
      DO09988k=1,nzon
      Ye(k)=0.d0
      DO09985i=1,Natom
      Ye(k)=Ye(k)+Zn(i)*Yabun(i,k)
09985 CONTINUE
09988 CONTINUE
      incnd=0
      DO09982k=nzon,2,-1
      taugam(k)=Capgam*Ye(k)*(UR*URHO)*RHO(k)*(RY(K)-RY(k-1))
      if(taugam(k).GT.30.D0.AND.incnd.EQ.0)incnd=k
09982 CONTINUE
      Rout=Homo*Ry(Nzon)
      Rcore=Homo*Ry(max(Kmnick,1))
      NPE=15*nint((Rout-Rcore)/Rout)
      NPE=min(NPE,15)
      NPE=max(NPE,2)
      NPC=15+15-NPE
      Pray(0)=0.D0
      Pray(NP+1)=ROUT
      DPE=(Rout-Rcore)/DBLE(NPE+1)
      DPC=Rcore/DBLE(15)
      DO09979i=1,15
      Pray(i)=Pray(i-1)+DPC
09979 CONTINUE
      DO09976i=15+1,Np
      Pray(i)=Pray(i-1)+DPE
09976 CONTINUE
      DO09973K=incnd+1,Nzon
      IF(K.GT.1)RM(K)=0.5D0*Homo*(Ry(K)+Ry(K-1))
      DO09970Lfr=1,Nfrgam
      HAPW(K,Lfr)=Capgam*Ye(k)*(UR*URHO)
      HAPabW(K,Lfr)=Capgam*Ye(k)*(UR*URHO)
09970 CONTINUE
09973 CONTINUE
      RM(1)=0.5D0*Homo*Ry(1)
      RM(0)=0.25D0*Homo*Ry(1)
      NEEDBR=.True.
      DO09967Lfr=1,Nfrgam
      Knth=incnd
      If(Knth.EQ.0)Then
      Rleft=0.d0
      else
      If(Knth.EQ.incnd)Then
      Rleft=Homo*Rcore
      else
      Rleft=Homo*Ry(Knth)
      endif
      endif
      KLASTS=Nzon
      DO09964Ip=NP,0,-1
      Kzer=Nzon
09961 IF(.NOT.(Pray(Ip).LT.RM(Kzer).AND.Kzer.GT.Knth))GOTO09960
      Kzer=Kzer-1
      GOTO09961
09960 CONTINUE
      Nrdial=max(Nzon-Kzer,1)
      DO09958K=Kzer+1,Nzon
      Amu(Ip,K)=SQRT(1.D0-(Pray(Ip)/RM(K))**2)
09958 CONTINUE
      Amu(Ip,Kzer)=0.d0
      K=Kzer
      If(K.LT.KlastS)then
      DO09955IK=K+1,KlastS
      NMU(IK)=Ip
09955 CONTINUE
      KlastS=K
      Elseif(K.GT.0)then
      Nmu(K)=15
      Endif
      Kz(Ip)=Kzer
09964 CONTINUE
      Krow=0
      Krowst(Np)=0
      DO09952Ip=NP,0,-1
      Kzer=Kz(Ip)
09952 CONTINUE
