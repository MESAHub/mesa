      dtau(Kpl,Ip)=(Amu(Ip,Kpl)*RM(Kpl)-Amu(Ip,Kpl-1)*RM(Kpl-1))*chiex*rho(Kpl)
      If(ScatG.AND.Nstep.GT.1)then
      fdirsumm=(chiab)
      elseif(KNadap.LT.0)then
      fdirsumm=XNifor(Kpl)*(1.d0-exp(-dtau(Kpl,Ip)/2.d0))
      else
      fdirsumm=(1.d0-exp(-dtau(Kpl,Ip)/2.d0))
      endif
      tauz=dtau(Kpl,Ip)/2.d0
      K=Kpl-1
09943 IF(.NOT.(K.GT.Kz(Ip)))GOTO09942
      chiab=HAPabW(K,Lfr)
      chiex=HAPW(K,Lfr)
      dtau(K,Ip)=(Amu(Ip,K)*RM(K)-Amu(Ip,K-1)*RM(K-1))*chiex*rho(K)
      If(ScatG.AND.Nstep.GT.1)then
      fdirsumm=fdirsumm+(chiab)
      elseif(KNadap.LT.0)then
      fdirsumm=fdirsumm+XNifor(K)*exp(-tauz)*(1.d0-exp(-dtau(K,Ip)))
      else
      fdirsumm=fdirsumm+exp(-tauz)*(1.d0-exp(-dtau(K,Ip)))
      endif
      tauz=tauz+dtau(K,Ip)
      K=K-1
      GOTO09943
09942 CONTINUE
      if(K.EQ.Kz(ip).AND.tauz.LT.1.d+02)then
09940 IF(.NOT.(tauz.LT.1.d+02.AND.K.LT.Nzon))GOTO09939
      K=K+1
      chiab=HAPabW(K,Lfr)
      chiex=HAPW(K,Lfr)
      dtau(K,Ip)=(Amu(Ip,K)*RM(K)-Amu(Ip,K-1)*RM(K-1))*chiex*rho(K)
      If(ScatG.AND.Nstep.GT.1)then
      tauz=tauz+dtau(K,Ip)
      fdirsumm=fdirsumm+(chiab)
      elseif(KNadap.LT.0)then
      tauz=tauz+dtau(K,Ip)
      fdirsumm=fdirsumm+XNifor(K)*exp(-tauz)*(1.d0-exp(-dtau(K,Ip)))
      else
      tauz=tauz+dtau(K,Ip)
      fdirsumm=fdirsumm+exp(-tauz)*(1.d0-exp(-dtau(K,Ip)))
      endif
      GOTO09940
09939 CONTINUE
      endif
      If(Kpl.GT.kz(ip))then
      FSdir(Ip,Kpl)=fdirsumm
      endif
09946 CONTINUE
09999 continue
09949 CONTINUE
      DO09937Kpl=incnd+1,Nzon
      DO09934Ip=Nmu(Kpl),0,-1
      Ki=max(Kpl,kz(Ip)+1)
      fr0=freqmn(Lfr)
      chiab=HAPabW(Ki,Lfr)
      chiex=HAPW(Ki,Lfr)
      dtau(Ki,Ip)=(Amu(Ip,Ki)*RM(Ki)-Amu(Ip,Ki-1)*RM(Ki-1))*chiex*rho(Ki)
      If(ScatG.AND.Nstep.GT.1)then
      fdirsumm=(chiab)
      elseif(KNadap.LT.0)then
      fdirsumm=XNifor(Ki)*(1.d0-exp(-dtau(Ki,Ip)/2.d0))
      else
      fdirsumm=(1.d0-exp(-dtau(Ki,Ip)/2.d0))
      endif
      tauz=dtau(Ki,Ip)/2.d0
      K=Ki
09931 IF(.NOT.(K.LT.Nzon))GOTO09930
      K=K+1
      chiab=HAPabW(K,Lfr)
      chiex=HAPW(K,Lfr)
      dtau(K,Ip)=(Amu(Ip,K)*RM(K)-Amu(Ip,K-1)*RM(K-1))*chiex*rho(K)
      If(ScatG.AND.Nstep.GT.1)then
      fdirsumm=fdirsumm+(chiab)
      elseif(KNadap.LT.0)then
      fdirsumm=fdirsumm+exp(-tauz)*XNifor(K)*(1.d0-exp(-dtau(K,Ip)))
      else
      fdirsumm=fdirsumm+exp(-tauz)*(1.d0-exp(-dtau(K,Ip)))
      endif
      tauz=tauz+dtau(K,Ip)
      GOTO09931
09930 CONTINUE
      If(Kpl.GT.kz(ip))then
      FSdir(Ip,Kpl)=(FSdir(Ip,Kpl)+fdirsumm)/2.d0
      endif
09934 CONTINUE
      DO09928Ip=0,NP
      xipl(Ip)=(Ip)
      If(Kpl.LE.kz(ip))then
      ftst(Ip)=ftst(max(0,Ip-1))
      fspl(Ip)=fspl(max(0,Ip-1))
      endif
09928 CONTINUE
09998 continue
09937 CONTINUE
      DO09925Ip=0,Np
      fcom(Ip,Lfr)=2.d0*FSdir(Ip,Nzon)
09925 CONTINUE
      DO09922K=1,Nzon
      If(K.LE.Knth)Then
      If(Knadap.LT.0)Then
      FJgam(k,2)=XNIfor(K)
      Else
      FJgam(k,2)=1.D0
      Endif
      Else
      FJgam(k,2)=0.D0
      DO09919Isw=NMU(K)-1,0,-1
      FJgam(k,2)=FJgam(k,2)+(FSdir(Isw,K)+FSdir(Isw+1,K))*(Amu(Isw,K)-Amu(Isw+1,K))
09919 CONTINUE
      FJgam(k,2)=FJgam(k,2)+2.D0*FSdir(NMU(K),K)*Amu(NMU(K),K)
      FJgam(k,2)=FJgam(k,2)*0.5D0
      endif
09922 CONTINUE
      IF(MOD(Nstep,900).EQ.0)THEN
      WRITE(4,'(''  K/   FJgam:'')')
      WRITE(4,'(8(1X,I3,''->'',1P,E9.2))')(K,FJgam(K,2),K=incnd+1,Nzon)
      ENDIF
09967 CONTINUE
09994 CONTINUE
      tnewg=t
      RETURN
