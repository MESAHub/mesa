!********************************************************
! PAQUETTE_COEFFICIENTS:
! Compute atomic diffusion coefficients, according to Paquette et al. (1986)
! The coefficients are derived from a screened resistive/attractive Coulomb potential.
! The input parameters are
! rho:            density [gcm^-3]
! T:            temperature [K]
! nn            number of elements
! CAN1:            mass [amu], note element NN is electrons
! KZN1:            charge [e]
! NA1:            number density [cm^-3]
! The output are the resistance coefficients in Burgers equations K_ij, z_ij, z'_ij and z''_ij
! It is also possible to output diffusion coefficients D_ij and thermal diffusion coeffcient
!  A_th used in Cowling&Chapman formalism, note Ath(NN,i) is Ath_ei

      subroutine paquette_coefficients(rho, T, NN, CAN1, KZN1, NA1, Ddiff, Kdiff, Zdiff, Zdiff1, Zdiff2, Ath)

      use const_def, only : pi4, boltzm, amu


! Input and output variables
      integer :: NN
      real(dp), intent(in) :: rho, T, CAN1(NN), KZN1(NN), NA1(NN)
      real(dp), intent(out) :: Ddiff(nn, nn), Kdiff(nn, nn), Zdiff(nn, nn), &
           Zdiff1(nn, nn), Zdiff2(nn, nn), Ath(nn, nn)
! Local variables
! Lengthscales for the plasma
      real(dp) :: LAMBDA_I2, LAMBDA_D2, LAMBDA2
! Ion density
      real(dp) :: NI
! Convenience, to precalculate as much as possible
      real(dp) :: KT, GAMMA2
! Collision integrals Omega (eqn. (18) in Paquette et al)
      real(dp) :: OMEGA1(3), OMEGA22, OMEGA2(NN)
! Dimensionless collision integrals (eqn. (65) in Paquette et al)
      real(dp) :: F1(3), F22
! Local variables
      real(dp) :: PSI_ST, EPS_ST, GAMMA_ST2, A, E_PSI_ST
      real(dp) :: AA, BB, CC, EE, Ps, Pt, Pst, Qs, Qt, Qst, Ss, &
           St, Ms, Mt, Xs, Xt, Xe, Me, Pe, Se, DELTA
      real(dp) :: DPSI_N1, DPSI_N
      integer :: i, J, N, K, NREF
      real(dp) :: NZZ, NE

      NE = NA1(NN)                  ! number density is per cm^3
      NI = 0.0D0
      NZZ = 0.0D0
      do i = 1, NN-1
         NI = NI + NA1(i)
         NZZ = NZZ + KZN1(i)*KZN1(i) * NA1(i)
      enddo

      KT = BOLTZM*T
! Typical distance between ions (squared)
      LAMBDA_I2 = pow(3.0D0/(pi4*NI),2.0D0/3.0D0)
! Debye length (squared)
      LAMBDA_D2 = (KT/(pi4 * qe*qe * (NE + NZZ)))
! Use max of lambda_i and lambda_d as typical distance
      LAMBDA2 = MAX(LAMBDA_D2, LAMBDA_I2)

      F22 = 1d0
! First evaluate the collision intergrals OMEGA22_SS, that appear in Ps (eq (9) of Paquette 1986)
      do I = 1, NN
         NREF = I
         GAMMA2 = pow2(4.0D0*KT/(qe*qe*KZN1(NREF)))*LAMBDA2
         GAMMA_ST2 = GAMMA2/pow2(KZN1(I))
! Reduced mass of the two particles
         A = (CAN1(I)*CAN1(NREF)/(CAN1(I)+CAN1(NREF)))            ! HH, in units of AMU
! Units of Omega: length^2 * velocity
         EPS_ST = pi4*LAMBDA2/GAMMA_ST2 * SQRT(2.0D0*KT/(pi4 * AMU*A))
! Find interpolation interval for dimensionless collision integral
         E_PSI_ST = log(1.0D0+GAMMA_ST2)
         PSI_ST = log(E_PSI_ST)
! Evaluate the collision integral
         if (PSI_ST.le.3.0D0) then
! if PSI_ST falls outside range of Paquette's fit, then just take border value
            PSI_ST = MAX(-7.D0, PSI_ST)
! Use spline interpolation to evaluate the collision integrals
! Determine interval in the table
            N = 1 + MIN(49, FLOOR((PSI_ST +7.d0)/0.2d0))
            DPSI_N1 = (-7.2D0 + 0.2D0*(N+1)) - PSI_ST
            DPSI_N = PSI_ST - (-7.2D0 + 0.2D0*N)
            if (NREF==NN .and. NREF/=I .or. I==NN .and. NREF/=I) then
! attractive potential (electron-ion)
               F22 = exp(DDAT(1,N)*DPSI_N1*DPSI_N1*DPSI_N1 + DDAT(2,N)*DPSI_N*DPSI_N*DPSI_N &
                    + DDAT(3,N)*DPSI_N1 + DDAT(4,N)*DPSI_N)
            else
! repulsive potential (ion-ion or electron-electron)
               F22 = exp(DD(1,N)*DPSI_N1*DPSI_N1*DPSI_N1 + DD(2,N)*DPSI_N*DPSI_N*DPSI_N &
                    + DD(3,N)*DPSI_N1 + DD(4,N)*DPSI_N)
            endif
         elseif (PSI_ST > 3.0D0 .and. PSI_ST < 4.0d0) then
! repulsive potential (ion-ion or electron-electron)
            if (NREF==NN .and. NREF/=I .or. I==NN .and. NREF/=I) then
               F22   = 2.08699D0*E_PSI_ST - 5.81444D0
            else
! attractive potential (electron-ion)
               F22   = 1.99016D0*E_PSI_ST - 4.56958D0
            endif
         elseif (PSI_ST >= 4.0D0) then
! repulsive and attractive coeffcients are the same in this range
            F22   = 1.99016D0*E_PSI_ST - 4.56958D0
         endif
         OMEGA2(I)   = EPS_ST*F22
      end do      ! I

      do K = 1, NN
         NREF = K
         GAMMA2 = pow2(4.0D0*KT/(qe*qe*KZN1(NREF))) * LAMBDA2
         do I = 1, NN
            GAMMA_ST2 = GAMMA2/pow2(KZN1(I))
! Reduced mass of the two particles
            A = (CAN1(I)*CAN1(NREF)/(CAN1(I)+CAN1(NREF)))            ! in units of AMU
! Units of Omega: length^2 * velocity
            EPS_ST = pi4*LAMBDA2/GAMMA_ST2 * SQRT(2.0D0*KT/(pi4 * AMU*A))
! Find interpolation interval for dimensionless collision integral
            E_PSI_ST = log(1.0D0+GAMMA_ST2)
            PSI_ST = log(E_PSI_ST)
! Evaluate the collision integral
            if (PSI_ST <= 3.0D0) then
               PSI_ST = MAX(-7.D0, PSI_ST)
               ! if PSI_ST falls outside range of Paquette fit, then just take border value, will probably never happen
! Use spline interpolation to evaluate the collision integrals
! Determine interval in the table
               N = 1 + MIN(49, FLOOR((PSI_ST +7.d0)/0.2d0))
               DPSI_N1 = (-7.2D0 + 0.2D0*(N+1)) - PSI_ST
               DPSI_N = PSI_ST - (-7.2D0 + 0.2D0*N)
               if (NREF==NN .and. NREF/=I .or. I==NN .and. NREF/=I) then
! attractive potential (electron-ion)
                  do J=1,3
                     F1(J) = exp(DCAT(1,N,J)*DPSI_N1*DPSI_N1*DPSI_N1 &
                               + DCAT(2,N,J)*DPSI_N*DPSI_N*DPSI_N &
                               + DCAT(3,N,J)*DPSI_N1 &
                               + DCAT(4,N,J)*DPSI_N)
                  end do
                  F22 = exp(DDAT(1,N)*DPSI_N1*DPSI_N1*DPSI_N1 &
                          + DDAT(2,N)*DPSI_N*DPSI_N*DPSI_N &
                          + DDAT(3,N)*DPSI_N1 &
                          + DDAT(4,N)*DPSI_N)
! repulsive potential (ion-ion or electron-electron)
               else
                  do J=1,3
                     F1(J) = exp(DC(1,N,J)*DPSI_N1*DPSI_N1*DPSI_N1 &
                               + DC(2,N,J)*DPSI_N*DPSI_N*DPSI_N &
                               + DC(3,N,J)*DPSI_N1 &
                               + DC(4,N,J)*DPSI_N)
                  end do
                  F22 = exp(DD(1,N)*DPSI_N1*DPSI_N1*DPSI_N1 &
                          + DD(2,N)*DPSI_N*DPSI_N*DPSI_N &
                          + DD(3,N)*DPSI_N1 &
                          + DD(4,N)*DPSI_N)
               endif
            elseif (PSI_ST > 3.0D0 .and. PSI_ST < 4.0d0) then
               if (NREF==NN .and. NREF/=I .or. I==NN .and. NREF/=I) then
! attractive potential (electron-ion)
                  F1(1) = 1.01101D0*E_PSI_ST - 3.19815D0
                  F1(2) = 1.04230D0*E_PSI_ST - 1.89637D0
                  F1(3) = 2.15672D0*E_PSI_ST - 2.81038D0
                  F22   = 2.08699D0*E_PSI_ST - 5.81444D0
               else
! repulsive potential (ion-ion or electron-electron)
                  F1(1) = 1.00141D0*E_PSI_ST - 3.18209D0
                  F1(2) = 0.99559D0*E_PSI_ST - 1.29553D0
                  F1(3) = 1.99814D0*E_PSI_ST - 0.64413D0
                  F22   = 1.99016D0*E_PSI_ST - 4.56958D0
               endif
            elseif (PSI_ST.ge.4.0D0) then
! repulsive and attractive coeffcients are the same in this range
               F1(1) = 1.00141D0*E_PSI_ST - 3.18209D0
               F1(2) = 0.99559D0*E_PSI_ST - 1.29553D0
               F1(3) = 1.99814D0*E_PSI_ST - 0.64413D0
               F22   = 1.99016D0*E_PSI_ST - 4.56958D0
            endif
            OMEGA1(:) = EPS_ST*F1(:)
            OMEGA22   = EPS_ST*F22                        ! for particle species NREF & K

! Binary diffusion coefficient for Chapman and Cowling formalism (first approximation)
            Ddiff(NREF, I) = 3.0D0*KT/(16.0D0*(NA1(NREF)+NA1(I))*(A*AMU)*OMEGA1(1))

            AA = OMEGA22/(5.D0*OMEGA1(1))
            BB = (5.D0*OMEGA1(2)-OMEGA1(3))/(5.D0*OMEGA1(1))
            CC = 2.D0*OMEGA1(2)/(5.D0*OMEGA1(1))-1.D0
            Xs = NA1(NREF)/(NA1(I)+NA1(NREF)) !number concentration of reference species NREF
            Xt = NA1(I)/(NA1(I)+NA1(NREF)) !number concentration of species I
            Ms = CAN1(NREF)/(CAN1(NREF)+CAN1(I))
            Mt = CAN1(I)/(CAN1(NREF)+CAN1(I))
            Pst = 3.0D0*(Ms-Mt)*(Ms-Mt) + 4.D0*Ms*Mt*AA
            EE = KT/(8.D0*Ms*Mt*OMEGA1(1))
            Ps = 8.D0*Ms*EE*OMEGA2(NREF)/(5.D0*KT)
            Pt = 8.D0*Mt*EE*OMEGA2(I)/(5.D0*KT)
            Ss = Ms*Ps-Mt*(3.D0*(Mt-Ms)+4.D0*Ms*AA)
            St = Mt*Pt-Ms*(3.D0*(Ms-Mt)+4.D0*Mt*AA)
            Qs = Ps*(6.D0*Mt*Mt + 5.D0*Ms*Ms - 4.D0*Ms*Ms*BB + 8.D0*Ms*Mt*AA)
            Qt = Pt*(6.D0*Ms*Ms + 5.D0*Mt*Mt - 4.D0*Mt*Mt*BB + 8.D0*Mt*Ms*AA)
            Qst = 3.D0*(Ms-Mt)*(Ms-Mt)*(5.D0-4.D0*BB) + 4.D0*Ms*Mt*AA*(11.D0-4.D0*BB) + 2.D0*Ps*Pt
            DELTA = 5.0D0*CC*CC*(Ms*Ms*Ps*Xs*Xs + Mt*Mt*Pt*Xt*Xt + Pst*Xs*Xt)/(Xs*Xs*Qs + Xt*Xt*Qt + Xs*Xt*Qst)

! second approximation
            Ddiff(NREF, I) = Ddiff(NREF, I)/(1.0D0 - DELTA)

! thermal coefficients
            Ath(NREF,I) = 5.D0*CC*(Xs*Ss-Xt*St)/(Xs*Xs*Qs+Xt*Xt*Qt+Xs*Xt*Qst)
! resistance coefficients for Burgers equations
            Kdiff(NREF, I) = 16.D0/3.D0*NA1(NREF)*NA1(I)*(A*AMU)*OMEGA1(1)
            Zdiff(NREF, I) = - CC
            Zdiff1(NREF, I) = -2.D0*BB+2.5D0
            Zdiff2(NREF, I) = 5.D0*AA

         end do !I
      end do !K
      return

      end subroutine paquette_coefficients
